#include "main.h"

/**
 *  * print_square - prints a square using the character #
 *
 *   * @size: size of the square
 *
 *   Return: Always 0 ( Success)
 */
void print_square(int size)
{
	if (size <= 0)
	{
	_putchar('\n');
	}
	else
	{
	int i;

	for (i = 0; i < size; i++)
	{
	_putchar('\n');
	}
	}
}
